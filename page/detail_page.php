			<h2 class="fs-23 text-uppercase text__gray py-3">NGUYỄN TRỌNG NHÂN: “TẬP LUYỆN THỂ HÌNH NHƯ TÁCH TRÀ NGON, CÀNG TẬP CÀNG NGHIỆN!”</h2>
			<p class="single-date">Posted on <a href="#" class="text-dark">20 Tháng Năm, 2019 by admin</a></p>
			<p class="fw-700 py-4 fst-italic fs-18">
				Gặp anh Nguyễn Trọng Nhân vào một buổi trưa Sài Gòn nắng gắt tại California Fitness & Yoga Centers (CFYC) lúc anh đang luyện tập. Ấn tượng đầu tiên của CFYC về anh Nhân là cơ thể tỉ lệ chuẩn và rắn chắc, 6 múi hiện hữu rõ ràng và ước mơ được một lần đặt chân lên sàn thi đấu của anh chàng. Nhưng để theo đuổi điều đó thì anh chàng đã bỏ lại sau lưng công việc tiếp viên hàng không “vạn người mê” và sự chống đối của gia đình. Hãy cùng CFYC tìm hiểu câu chuyện của anh qua bài viết sau.
			</p>
			<h5 class="fs-23 text__gray fw-700">
				Từ bỏ công việc tiếp viên hàng không “triệu người theo đuổi” để một lần được sống với đam mê
			</h5>
			<p class="py-4 fs-18 text-dark">
				Là một Gymer đã ngót nghét 10 năm, trong đó đã có 4 năm gắn bó với CFYC và đang theo đuổi con đường thi đấu thể hình chuyên nghiệp nhưng hiếm ai biết rằng công việc trước đây của anh Nhân là một tiếp viên hàng không. <span class="fst-italic">“Xuất phát điểm là nhân viên bán hàng tại sân bay nhưng khi nhìn những tà áo tiếp viên lướt qua mỗi ngày khiến tôi nhen nhóm suy nghĩ muốn thử sức. Với ngoại ngữ khá, thân hình ổn của mình, tôi nghĩ tại sao lại không? Và may mắn đã mỉm cười khi tôi trở thành thành viên của đại gia đình Vietjet Air” – anh Nhân chia sẻ.</span>
			</p>
			<p class="fst-italic fs-18 text-dark">
				Anh Nhân khoác áo tiếp viên hàng không của Vietjet Air
			</p>
			<p class="py-4 fs-18 text-dark">
				Những tưởng là đã tìm được một bệ phóng an toàn cho sự nghiệp sau này nhưng qua thời gian luyện tập thể hình cùng nghiên cứu thực phẩm bổ sung, anh Nhân nhận ra đã có sự thay đổi nhất định trong suy nghĩ về công việc <span class="fst-italic">“nuôi sống bản thân” của mình. Anh bộc bạch :“Lúc ban đầu đến với bộ môn Gym vốn là mục đích để khoẻ người, xoã stress. Uống thực phẩm chức năng để phụ trợ cho hiệu quả của việc tập luyện. Nhưng nghiên cứu mãi, tập luyện mãi thành đam mê lúc nào không hay.”</span> Và thế, sau hai năm làm việc tại hãng hàng không Vietjet, anh chàng Nguyễn Trọng Nhân quyết định rẽ hướng và nuôi mơ ước trở thành vận động viên thể hình.
			</p>
			<p class="fst-italic fs-18 text-dark">
				“Nhưng nghiên cứu mãi, tập luyện mãi thành đam mê lúc nào không hay” – Nguyễn Trọng Nhân
			</p>
			<p class="py-4 fs-18 text-dark">
				Khoảng thời gian đó có thể nói là khá buồn với anh Nhân, anh dường như có thể cảm nhận được cụm từ <span class="fst-italic">cả thế giới quay lưng”</span> khi bạn bè lẫn gia đình đều chống đối sự thay đổi này của chàng trai trẻ. <span class="fst-italic">“Nhưng biết làm sao được, đam mê nó cứ ngấm vào tôi từng chút, nếu say rượu một lần là tỉnh, thì tập luyện thể hình như trà ngon, càng tập tôi càng nghiện”</span> – anh Nhân khẳng định.
			</p>
			<h5 class="fs-23 text__gray fw-700">
				Quá trình chuẩn bị thi đấu để bùng nổ vào năm 2019
			</h5>

			<p class="py-4 fs-18 text-dark">
				Bỏ qua những lời điều tiếng, dèm pha về quyết định được cho là “ngốc nghếch” của bản thân, chàng “phi công” vẫn tập trung toàn sức lực của mình cho những giải đấu thể hình sắp tới trong năm 2019  <span class="fst-italic">“Tập 10 năm rồi, cuối cùng tôi cũng đủ tiêu chuẩn để “lên sàn”, vậy thì có gì là ngốc nghếch chứ?”</span> – anh Nhân nói.
			</p>

			<p class="fst-italic fs-18 text-dark">
				Cánh tay rắn chắc đáng ngưỡng mộ của anh Nguyễn Trọng Nhân
			</p>
			<p class="py-4 fs-18 text-dark">
				Khi được hỏi về phác đồ luyện tập, anh Nhân cũng thành thật chia sẻ:   <span class="fst-italic">“Thật ra lúc đầu do không có người hướng dẫn nên tập càng lúc càng sai, có những phần tôi cố ý tập để to hơn nhưng lại to phần khác. Thời gian đó, tôi chỉ muốn từ bỏ tất cả, nhưng khi đến với CFYC, được sự hướng dẫn từ PT lẫn tự nghiên cứu thì hiện tại tôi đã có vài thành tựu nhất định hơn trong hình thể”</span>
			</p>
			<p class="py-4 fs-18 text-dark">
				<span class="fst-italic">“Tôi luôn muốn được tham gia những giải đấu lớn, từ đó truyền cảm hứng tập luyện của mình đến những bạn trẻ yêu gym xung quanh tôi và giúp đỡ, tạo động lực để các bạn giảm thiếu sự nhút nhát trong việc dám ước mơ. Tôi có thể từ bỏ công việc ngàn đô, bạn thì sao?”</span>
			</p>
			<p class="py-4 fs-18 text-dark">
				Lợi thế của anh Nhân hơn những vận động viên thể hình mới khác chính là anh sở hữu một cửa hàng chuyên kinh doanh thực phẩm chức năng. Để có khả năng tư vấn cho khách hàng của mình, anh Nhân bắt buộc phải tìm hiểu tất cả kiến thức liên quan, cập nhật những xu hướng hình thể mới nhất trong ngành. Và cũng từ đó anh có một lượng thông tin khổng lồ về Gym, anh cũng nói thêm:
				<span class="fst-italic">“Từ sau khi tôi nghỉ Vietjet, công việc kinh doanh là thu nhập chính của tôi.”</span>
			</p>
			<h5 class="fs-23 text__gray fw-700">
				Tổng động viên bà xã tham gia BTC22 để cùng khỏe – đẹp
			</h5>
			<p class="py-4 fs-18 text-dark">
				Khi nhắc đến những quyết định quan trọng trong cuộc sống để đến với niềm đam mê về thể hình, anh Nhân lúc muốn gửi lời cảm ơn đến người vợ luôn đứng đằng sau ủng hộ. Anh tâm sự:
				<span class="fst-italic">“Những ngày đầu khi bị mọi người phản đối, tôi bị stress lắm nhưng mà may vẫn có cô ấy ở bên cạnh và động viên. Để có thể giúp cô ấy hiểu được tình yêu của tôi dành cho gym, mỗi lần cả hai rãnh rỗi chúng tôi hay đến CFYC để tập luyện. Vợ tôi từ người không hiểu và yêu thích thể thao thì giờ đâm ra nghiện luyện tập luôn.”</span>
			</p>
			<p class="py-4 fs-18 text-dark">
				Để tiếp lửa cho niềm đam mê luyện tập, anh Nhân đã mua hẵng thẻ tập 05 năm và động viên chị tham gia BTC22. Anh chia sẻ:
				<span class="fst-italic">“BTC22 là một cuộc thi được tổ chức rất chuyên nghiệp. Tôi mong muốn vợ mình khi tham gia cũng sẽ đạt được hình thể như cô ấy mong muốn và tăng thêm niềm đam mê cho việc luyện tập thể dục thể thao.”</span>
			</p>
			<p class="py-4 fs-18 text-dark mb-5">
				Nghe câu chuyện của anh Nhân, CFYC thật lòng ngưỡng mộ bởi lòng quả cảm anh có trong công việc lẫn ước mơ. Chúc anh Nhân ngày càng thành công trong con đường mình đã chọn.
				
